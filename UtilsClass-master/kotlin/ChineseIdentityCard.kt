    private val birthDay: Int
    private val randomCode: Int
    private val verifyCode: String


    init {
        // Kotlin 对嵌套捕获分组的顺序似乎有点不一样
        // 同时又不支持命名分组
        val regex = "^(?<location>(?<block>\\d)\\d{5})(?<birth>(?<year>\\d{4})(?<month>\\d{2})(?<day>\\d{2}))(?<randomCode>\\d{3})(?<verfyCode>[0-9Xx])\$".toRegex()
        val matchResult = regex.matchEntire(idNumber) ?: throw IdNumberErrorException("Format Error")

        this.locationCode = matchResult.groupValues[1].toInt()
        this.birthYear = matchResult.groupValues[4].toInt()
        this.birthMonth = matchResult.groupValues[5].toInt()
        this.birthDay = matchResult.groupValues[6].toInt()
        this.randomCode = matchResult.groupValues[7].toInt()
        this.verifyCode = matchResult.groupValues[8]

        // 验证地区码
        if (locationMap[this.locationCode] == null) {
            throw IdNumberErrorException("Location Code Error")
        }

        // 验证生日
        if (this.birthDay !in 1..31 ||
                this.birthMonth !in 1..12 ||
                this.birthYear !in 1900..3000) {
            throw IdNumberErrorException("Birth Code Error")
        }

        // 验证有效性
        verify(idNumber)
    }

    val birthday: Date
        get() {
            val calendar = Calendar.getInstance()
            calendar.set(this.birthYear, this.birthMonth, this.birthDay)
            return calendar.time
        }

    val gender: Gender
        get() =
            if ((this.randomCode and 1) == 1) {
                Gender.Male
            } else {
                Gender.Female
            }

    val location: String
        get() {
            val result = StringBuilder()
            result.append(locationMap[this.locationCode / 100000 * 100000]).append(' ')
            result.append(locationMap[this.locationCode / 1000 * 1000]).append(' ')
            result.append(locationMap[this.locationCode / 100 * 100]).append(' ')
            result.append(locationMap[this.locationCode])
            return result.toString()
        }

    override fun toString(): String =
            "${this.birthday} ${this.gender} ${this.location}"
}

enum class Gender {
    Male, Female
}

class IdNumberErrorException(reason: String = "") : RuntimeException(reason)
