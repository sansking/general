        @JvmStatic
        @Throws(IdNumberErrorException::class)
        fun verify(idNumber: String) {
            val wi = listOf(7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2)
            val ai = idNumber.toCharArray()
            val verifyCodeMap = listOf('1', '0', 'X', '9', '8', '7', '6', '5', '4', '3', '2')

            if (!"^\\d{17}[0-9Xx]$".toRegex().matches(idNumber)) {
                throw IdNumberErrorException("ID Number Error")
            }

            var checksum: Int = 0
            wi.forEachIndexed { index, w ->
                checksum += ((ai[index] - '0').toInt() * w)
            }

            val targetVerifyCode = verifyCodeMap[checksum % 11]

            if (targetVerifyCode != idNumber[17]) {
                throw IdNumberErrorException("Verify Code Error")
            }
        }

